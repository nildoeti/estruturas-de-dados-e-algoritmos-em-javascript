// exemplo de laço for
// exibe o valor de i, enquanto i for menor que 10
for (var i = 0; i < 10; i++) {
  console.log(i);
}

console.log('\n\n');

// exemplo do laço while
// o bloco de código dentro do laço while será executado enquanto a condição for
// verdadeira para i menor ou igual a 10
var i = 0;
while (i <= 10) {
  console.log(i);
  i++;
}

console.log('\n\n');

// laco do...while
var x = 0;
do {
  console.log(x);
} while (x);
